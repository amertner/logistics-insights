if [[ "$ENABLE_BENCH_PROFILER" == "true" ]]; then
  printf "${C_CYAN}Per-task results appended to %s${C_RESET}\n" "$PER_TASK_CSV"
  printf "${C_CYAN}Per-tick stats appended to %s${C_RESET}\n" "$TICK_STATS_CSV"
  printf "${C_CYAN}Per-call stats appended to %s${C_RESET}\n" "$PER_CALL_CSV"
fi
