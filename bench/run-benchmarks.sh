#!/usr/bin/env bash
#
# Sweep logistics-insights mod settings against a Factorio save and record timings.
#
# For each configuration in CONFIGURATIONS, this script:
#   1. Writes bench-overrides.lua in the mod folder with the corresponding accessor overrides.
#   2. Runs Factorio with --benchmark and captures stdout.
#   3. Parses the avg/min/max ms-per-update from the benchmark summary.
#   4. Appends a row to bench/results.csv.
#
# bench-overrides.lua is removed in a trap handler, even on Ctrl-C.
#
# Configure the variables in bench/configs.local.sh (see configs.local.sh.example).

set -euo pipefail

# ============================== CONFIGURATION ==============================

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
MOD_DIR="$(dirname "$SCRIPT_DIR")"
OVERRIDE_LUA="$MOD_DIR/bench-overrides.lua"
RESULTS_CSV="$SCRIPT_DIR/results.csv"
PER_TASK_CSV="$SCRIPT_DIR/per-task-results.csv"
TICK_STATS_CSV="$SCRIPT_DIR/per-tick-stats.csv"
PER_CALL_CSV="$SCRIPT_DIR/per-call-stats.csv"

# Defaults — overridden by configs.local.sh
FACTORIO_EXE=""
SAVE_FILE=""
FACTORIO_LOG=""
DISABLE_TASKS=()
CONFIGURATIONS=("label=default chunk_size=400")

# Run shape — rarely need per-machine override
BENCHMARK_TICKS=6000
BENCHMARK_RUNS=3      # forced to 1 when ENABLE_BENCH_PROFILER is set
ENABLE_BENCH_PROFILER=true

# Per-task console display threshold (ms). Tasks below this are collapsed.
PER_TASK_DISPLAY_THRESHOLD_MS=5.0

# Source per-machine overrides if present.
if [[ -f "$SCRIPT_DIR/configs.local.sh" ]]; then
  # shellcheck source=configs.local.sh.example
  source "$SCRIPT_DIR/configs.local.sh"
fi

# ===========================================================================

# ---- ANSI colours (disabled when stdout is not a terminal) ----
if [[ -t 1 ]]; then
  C_CYAN='\033[36m'
  C_GREEN='\033[32m'
  C_DGREEN='\033[32;2m'
  C_YELLOW='\033[33m'
  C_DYELLOW='\033[33;2m'
  C_DGRAY='\033[90m'
  C_RED='\033[31m'
  C_RESET='\033[0m'
else
  C_CYAN='' C_GREEN='' C_DGREEN='' C_YELLOW='' C_DYELLOW=''
  C_DGRAY='' C_RED='' C_RESET=''
fi

# ---- Helpers ----

# Extract a value from a space-delimited key=value config string.
config_get() {
  local config="$1" key="$2"
  local pair
  for pair in $config; do
    case "$pair" in
      "${key}"=*) echo "${pair#*=}"; return 0 ;;
    esac
  done
  return 1
}

# Build semicolon-separated key=value string (excluding label) for CSV.
config_str() {
  local config="$1" result="" pair
  for pair in $config; do
    case "$pair" in
      label=*) continue ;;
      *) result="${result:+${result};}${pair}" ;;
    esac
  done
  echo "$result"
}

# ISO 8601 timestamp with timezone offset (portable across GNU/BSD date).
timestamp() {
  if date --version >/dev/null 2>&1; then
    # GNU date
    date +"%Y-%m-%dT%H:%M:%S%:z"
  else
    # BSD date (macOS) — %z gives +0100, insert colon
    date +"%Y-%m-%dT%H:%M:%S%z" | sed 's/\([+-][0-9][0-9]\)\([0-9][0-9]\)$/\1:\2/'
  fi
}

# Write bench-overrides.lua for a given configuration.
write_override_file() {
  local config="$1" enable_profiler="$2" dump_after="$3"
  local label pair key value lua_val

  label=$(config_get "$config" "label")

  {
    echo "-- Auto-generated by bench/run-benchmarks.sh. Do not edit."
    echo "-- Label: $label"

    if [[ "$enable_profiler" == "true" ]]; then
      echo "local bench_profiler = require('scripts.bench-profiler')"
      echo "bench_profiler.enabled = true"
      echo "bench_profiler.dump_after_ticks = $dump_after"

      if [[ ${#DISABLE_TASKS[@]} -gt 0 ]]; then
        echo "bench_profiler.task_interval_overrides = {"
        local task
        for task in "${DISABLE_TASKS[@]}"; do
          echo "  ['$task'] = 999999999,"
        done
        echo "}"
      fi
    fi

    echo "return function(global_data)"
    for pair in $config; do
      key="${pair%%=*}"
      value="${pair#*=}"
      [[ "$key" == "label" ]] && continue
      # Booleans pass through as Lua keywords; everything else is literal.
      case "$value" in
        true|false) lua_val="$value" ;;
        *)          lua_val="$value" ;;
      esac
      echo "  global_data.$key = function() return $lua_val end"
    done
    echo "end"
  } > "$OVERRIDE_LUA"
}

# ---- Validation ----

if [[ "$ENABLE_BENCH_PROFILER" == "true" && "$BENCHMARK_RUNS" -ne 1 ]]; then
  printf "${C_YELLOW}Forcing BENCHMARK_RUNS=1 because ENABLE_BENCH_PROFILER is set.${C_RESET}\n"
  BENCHMARK_RUNS=1
fi

if [[ -z "$FACTORIO_EXE" || ! -f "$FACTORIO_EXE" ]]; then
  printf "${C_RED}ERROR: Factorio executable not found at: %s${C_RESET}\n" "$FACTORIO_EXE" >&2
  exit 1
fi
if [[ -z "$SAVE_FILE" || ! -f "$SAVE_FILE" ]]; then
  printf "${C_RED}ERROR: Save file not found at: %s${C_RESET}\n" "$SAVE_FILE" >&2
  exit 1
fi

# ---- CSV headers ----

if [[ ! -f "$RESULTS_CSV" ]]; then
  echo "timestamp,label,config,updates,total_ms,avg_ms,min_ms,max_ms" > "$RESULTS_CSV"
fi
if [[ "$ENABLE_BENCH_PROFILER" == "true" && ! -f "$PER_TASK_CSV" ]]; then
  echo "timestamp,label,config,task,count,total_ms,avg_ms_per_call,dump_tick" > "$PER_TASK_CSV"
fi
if [[ "$ENABLE_BENCH_PROFILER" == "true" && ! -f "$TICK_STATS_CSV" ]]; then
  echo "timestamp,label,config,active_ticks,p50_ms,p90_ms,p95_ms,p99_ms,max_ms,max_tick,over_5ms,over_10ms,over_50ms" > "$TICK_STATS_CSV"
fi
if [[ "$ENABLE_BENCH_PROFILER" == "true" && ! -f "$PER_CALL_CSV" ]]; then
  echo "timestamp,label,config,task,count,p50_ms,p90_ms,p95_ms,p99_ms,max_ms,max_tick" > "$PER_CALL_CSV"
fi

# ---- Cleanup trap ----

cleanup() {
  if [[ -f "$OVERRIDE_LUA" ]]; then
    rm -f "$OVERRIDE_LUA"
    printf "\n${C_DGRAY}Removed %s${C_RESET}\n" "$OVERRIDE_LUA"
  fi
}
trap cleanup EXIT

# ---- Awk helper: convert Duration value+unit to ms ----
# Used inline in awk scripts. Expects val in $1, unit in $2 variables
# within the awk scope. Returns ms via the to_ms() function.
AWK_TO_MS='
function to_ms(val, unit) {
  if (unit == "s")  return val * 1000.0
  if (unit == "ms") return val
  if (unit == "us") return val / 1000.0
  if (unit == "ns") return val / 1000000.0
  return val
}
function parse_duration(token,    val, unit, p) {
  p = match(token, /^[0-9.]+/)
  val = substr(token, 1, RLENGTH) + 0
  unit = substr(token, RLENGTH + 1)
  return to_ms(val, unit)
}
'

# ---- Awk helper: combsort (portable, no gawk asort needed) ----
AWK_COMBSORT='
function combsort(arr, n,    gap, i, swapped, tmp) {
  gap = n; swapped = 1
  while (gap > 1 || swapped) {
    gap = int(gap / 1.3)
    if (gap < 1) gap = 1
    swapped = 0
    for (i = 0; i + gap < n; i++) {
      if (arr[i] > arr[i + gap]) {
        tmp = arr[i]; arr[i] = arr[i + gap]; arr[i + gap] = tmp
        swapped = 1
      }
    }
  }
}
function percentile(sorted, n, p,    idx) {
  idx = int(n * p / 100.0 + 0.999999) - 1
  if (idx < 0) idx = 0
  if (idx >= n) idx = n - 1
  return sorted[idx]
}
'

# ============================== MAIN LOOP ==============================

for config in "${CONFIGURATIONS[@]}"; do
  label=$(config_get "$config" "label")
  cstr=$(config_str "$config")

  printf "\n${C_CYAN}=== %s ===${C_RESET}\n" "$label"

  dump_after=$((BENCHMARK_TICKS - 60))
  write_override_file "$config" "$ENABLE_BENCH_PROFILER" "$dump_after"

  # Truncate the Factorio log so the dump-parser only sees this run's output.
  if [[ "$ENABLE_BENCH_PROFILER" == "true" && -n "$FACTORIO_LOG" && -f "$FACTORIO_LOG" ]]; then
    : > "$FACTORIO_LOG"
  fi

  factorio_args=(
    --benchmark "$SAVE_FILE"
    --benchmark-ticks "$BENCHMARK_TICKS"
    --benchmark-runs "$BENCHMARK_RUNS"
    --disable-audio
  )
  printf "Running: %s %s\n" "$FACTORIO_EXE" "${factorio_args[*]}"
  output=$("$FACTORIO_EXE" "${factorio_args[@]}" 2>&1) || true

  # ---- Parse benchmark summary ----
  updates=""
  total_ms=""
  avg_ms=""
  min_ms=""
  max_ms=""

  while IFS= read -r line; do
    if [[ "$line" =~ Performed[[:space:]]+([0-9]+)[[:space:]]+updates[[:space:]]+in[[:space:]]+([0-9.]+)[[:space:]]*ms ]]; then
      updates="${BASH_REMATCH[1]}"
      total_ms="${BASH_REMATCH[2]}"
    fi
    if [[ "$line" =~ avg:[[:space:]]*([0-9.]+)[[:space:]]*ms.*min:[[:space:]]*([0-9.]+)[[:space:]]*ms.*max:[[:space:]]*([0-9.]+)[[:space:]]*ms ]]; then
      avg_ms="${BASH_REMATCH[1]}"
      min_ms="${BASH_REMATCH[2]}"
      max_ms="${BASH_REMATCH[3]}"
    fi
  done <<< "$output"

  if [[ -z "$avg_ms" ]]; then
    printf "${C_YELLOW}WARNING: Could not parse benchmark output for %s. Raw output:${C_RESET}\n" "$label"
    echo "$output"
    continue
  fi

  printf "${C_GREEN}  updates=%s  total=%s ms  avg=%s ms  min=%s ms  max=%s ms${C_RESET}\n" \
    "$updates" "$total_ms" "$avg_ms" "$min_ms" "$max_ms"

  ts=$(timestamp)
  printf "%s,%s,%s,%s,%s,%s,%s,%s\n" \
    "$ts" "$label" "$cstr" "$updates" "$total_ms" "$avg_ms" "$min_ms" "$max_ms" \
    >> "$RESULTS_CSV"

  # ---- Bench profiler analysis ----

  if [[ "$ENABLE_BENCH_PROFILER" != "true" ]]; then
    continue
  fi

  if [[ -z "$FACTORIO_LOG" || ! -f "$FACTORIO_LOG" ]]; then
    printf "${C_YELLOW}WARNING: Factorio log not found at: %s${C_RESET}\n" "$FACTORIO_LOG"
    continue
  fi

  # ---- Per-task dump ([libench-dump] lines) ----
  # Output: tab-separated lines "task\tcount\ttotal_ms\ttick"
  # Only lines from the highest tick value (the final dump).
  dump_data=$(grep -E '\[libench-dump\]' "$FACTORIO_LOG" 2>/dev/null | awk "$AWK_TO_MS"'
    {
      # Parse: [libench-dump] tick=N task=NAME count=N time=Duration: VALunit
      tick = ""; task = ""; count = ""
      for (i = 1; i <= NF; i++) {
        if ($i ~ /^tick=/)  { split($i, a, "="); tick = a[2] + 0 }
        if ($i ~ /^task=/)  { split($i, a, "="); task = a[2] }
        if ($i ~ /^count=/) { split($i, a, "="); count = a[2] + 0 }
      }
      # Duration token is the last field (e.g. "16.101400ms")
      dur_token = $NF
      ms = parse_duration(dur_token)

      if (tick > max_tick) max_tick = tick
      n++
      ticks[n] = tick; tasks[n] = task; counts[n] = count; times[n] = ms
    }
    END {
      for (i = 1; i <= n; i++) {
        if (ticks[i] == max_tick) {
          printf "%s\t%d\t%.6f\t%d\n", tasks[i], counts[i], times[i], ticks[i]
        }
      }
    }
  ' || true)

  if [[ -z "$dump_data" ]]; then
    printf "${C_YELLOW}WARNING: No bench profiler dump found in %s. Did the run reach 60 ticks?${C_RESET}\n" "$FACTORIO_LOG"
  else
    # Extract dump tick from first line
    dump_tick=$(echo "$dump_data" | head -1 | cut -f4)
    printf "${C_GREEN}  Per-task (dump @ tick %s):${C_RESET}\n" "$dump_tick"

    omitted_names=()
    omitted_total=0

    while IFS=$'\t' read -r task count total_task_ms tick; do
      if [[ -z "$task" ]]; then continue; fi
      avg_per_call=$(awk "BEGIN{printf \"%.6f\", ($count > 0 ? $total_task_ms / $count : 0)}")

      above=$(awk "BEGIN{print ($total_task_ms >= $PER_TASK_DISPLAY_THRESHOLD_MS) ? 1 : 0}")
      if [[ "$above" == "1" ]]; then
        printf "${C_DGREEN}    %-30s count=%6d  total=%9.3f ms  avg=%8.4f ms/call${C_RESET}\n" \
          "$task" "$count" "$total_task_ms" "$avg_per_call"
      else
        omitted_names+=("$task")
        omitted_total=$(awk "BEGIN{printf \"%.1f\", $omitted_total + $total_task_ms}")
      fi

      printf "%s,%s,%s,%s,%d,%.4f,%.6f,%s\n" \
        "$ts" "$label" "$cstr" "$task" "$count" "$total_task_ms" "$avg_per_call" "$tick" \
        >> "$PER_TASK_CSV"
    done <<< "$dump_data"

    if [[ ${#omitted_names[@]} -gt 0 ]]; then
      omitted_list=$(IFS=", "; echo "${omitted_names[*]}")
      printf "${C_DGRAY}    (%d tasks omitted < %s ms: %s - %s ms total)${C_RESET}\n" \
        "${#omitted_names[@]}" "$PER_TASK_DISPLAY_THRESHOLD_MS" "$omitted_list" "$omitted_total"
    fi
  fi

  # ---- Per-tick stats ([libench-tick] lines) ----
  # Awk outputs a header line then spike tick numbers, one per line.
  tick_stats_raw=$(grep -E '\[libench-tick\]' "$FACTORIO_LOG" 2>/dev/null | awk "$AWK_TO_MS$AWK_COMBSORT"'
    {
      # [libench-tick] tick=N time=Duration: VALunit
      for (i = 1; i <= NF; i++) {
        if ($i ~ /^tick=/) { split($i, a, "="); tick = a[2] + 0 }
      }
      ms = parse_duration($NF)
      n++
      vals[n-1] = ms
      tick_nums[n-1] = tick
    }
    END {
      if (n == 0) { print "EMPTY"; exit }

      # Find max and its tick (before sorting).
      max_v = 0; max_tick = 0
      for (i = 0; i < n; i++) {
        if (vals[i] > max_v) { max_v = vals[i]; max_tick = tick_nums[i] }
      }

      # Identify spike ticks (> 5 ms) before sorting.
      spike_count = 0
      for (i = 0; i < n; i++) {
        if (vals[i] > 5) { spikes[spike_count++] = tick_nums[i] }
      }

      # Count thresholds before sorting.
      over5 = 0; over10 = 0; over50 = 0
      for (i = 0; i < n; i++) {
        if (vals[i] > 50) over50++
        if (vals[i] > 10) over10++
        if (vals[i] >  5) over5++
      }

      # Sort for percentiles.
      combsort(vals, n)

      printf "STATS\t%d\t%.4f\t%.4f\t%.4f\t%.4f\t%.4f\t%d\t%d\t%d\t%d\n", \
        n, percentile(vals,n,50), percentile(vals,n,90), percentile(vals,n,95), \
        percentile(vals,n,99), max_v, max_tick, over5, over10, over50

      for (i = 0; i < spike_count; i++) {
        printf "SPIKE\t%d\n", spikes[i]
      }
    }
  ' || true)

  # Parse tick stats output.
  active_ticks="" p50="" p90="" p95="" p99="" tick_max="" tick_max_tick=""
  over_5ms="" over_10ms="" over_50ms=""
  spike_ticks=()

  while IFS=$'\t' read -r tag rest; do
    case "$tag" in
      EMPTY) break ;;
      STATS)
        IFS=$'\t' read -r active_ticks p50 p90 p95 p99 tick_max tick_max_tick over_5ms over_10ms over_50ms <<< "$rest"
        ;;
      SPIKE)
        spike_ticks+=("$rest")
        ;;
    esac
  done <<< "$tick_stats_raw"

  # ---- Queue stats ([libench-queue] lines) ----
  queue_stats_raw=$(grep -E '\[libench-queue\]' "$FACTORIO_LOG" 2>/dev/null | awk '
    {
      for (i = 1; i <= NF; i++) {
        if ($i ~ /^first=/)          { split($i, a, "="); first = a[2] + 0 }
        if ($i ~ /^last=/)           { split($i, a, "="); last = a[2] + 0 }
        if ($i ~ /^max_heavy=/)      { split($i, a, "="); mh = a[2] + 0 }
        if ($i ~ /^max_heavy_tick=/) { split($i, a, "="); mht = a[2] + 0 }
        if ($i ~ /^overflow=/)       { split($i, a, "="); ov = a[2] + 0 }
      }
      n++
      printf "QUEUE\t%d\t%d\t%d\t%d\t%d\n", first, last, mh, mht, ov
      total_ov += ov
      if (ov > 0) queues_with_ov++
      if (mh > worst_mh) { worst_mh = mh; worst_mht = mht }
    }
    END {
      if (n == 0) { print "EMPTY"; exit }
      printf "SUMMARY\t%d\t%d\t%d\t%d\t%d\n", n, queues_with_ov, total_ov, worst_mh, worst_mht
    }
  ' || true)

  queue_count="" queues_with_overflow="" total_overflow="" worst_max_heavy="" worst_max_heavy_tick=""
  queue_records=()

  while IFS=$'\t' read -r tag rest; do
    case "$tag" in
      EMPTY) break ;;
      QUEUE) queue_records+=("$rest") ;;
      SUMMARY)
        IFS=$'\t' read -r queue_count queues_with_overflow total_overflow worst_max_heavy worst_max_heavy_tick <<< "$rest"
        ;;
    esac
  done <<< "$queue_stats_raw"

  # Display queue summary.
  if [[ -n "$queue_count" ]]; then
    if [[ "$total_overflow" -gt 0 || "$worst_max_heavy" -gt 1 ]]; then
      printf "${C_YELLOW}  Queue: builds=%s with_overflow=%s total_overflow=%s worst_max_heavy=%s (tick %s)${C_RESET}\n" \
        "$queue_count" "$queues_with_overflow" "$total_overflow" "$worst_max_heavy" "$worst_max_heavy_tick"
    else
      printf "${C_DGREEN}  Queue: builds=%s (no collisions, no overflow)${C_RESET}\n" "$queue_count"
    fi
  fi

  # Display tick stats.
  if [[ -n "$active_ticks" ]]; then
    printf "${C_GREEN}  LI per-tick: active=%s p50=%s p90=%s p95=%s p99=%s max=%s ms (tick %s)  >5ms:%s >10ms:%s >50ms:%s${C_RESET}\n" \
      "$active_ticks" "$p50" "$p90" "$p95" "$p99" "$tick_max" "$tick_max_tick" \
      "$over_5ms" "$over_10ms" "$over_50ms"

    # Spike correlation with queue data.
    if [[ -n "$queue_count" && ("$total_overflow" -gt 0 || "$worst_max_heavy" -gt 1) && ${#spike_ticks[@]} -gt 0 ]]; then
      same_as_max=0
      with_overflow=0
      hit_count=0
      for spike in "${spike_ticks[@]}"; do
        for qrec in "${queue_records[@]}"; do
          IFS=$'\t' read -r qfirst qlast qmh qmht qov <<< "$qrec"
          if [[ "$spike" -ge "$qfirst" && "$spike" -le "$qlast" ]]; then
            hit_count=$((hit_count + 1))
            if [[ "$spike" -eq "$qmht" ]]; then same_as_max=$((same_as_max + 1)); fi
            if [[ "$qov" -gt 0 ]]; then with_overflow=$((with_overflow + 1)); fi
            break
          fi
        done
      done
      if [[ "$hit_count" -gt 0 ]]; then
        printf "${C_YELLOW}  Spike correlation: %d spike ticks; %d hit their queue's max_heavy_tick; %d were in queues with overflow > 0${C_RESET}\n" \
          "$hit_count" "$same_as_max" "$with_overflow"
      fi
    fi

    printf "%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s\n" \
      "$ts" "$label" "$cstr" "$active_ticks" \
      "$p50" "$p90" "$p95" "$p99" "$tick_max" "$tick_max_tick" \
      "$over_5ms" "$over_10ms" "$over_50ms" \
      >> "$TICK_STATS_CSV"
  fi

  # ---- Per-call stats ([libench-call] lines) ----
  # Awk groups by task and computes percentiles; also outputs per-tick records for spike attribution.
  per_call_raw=$(grep -E '\[libench-call\]' "$FACTORIO_LOG" 2>/dev/null | awk "$AWK_TO_MS$AWK_COMBSORT"'
    {
      # [libench-call] task=NAME tick=N time=Duration: VALunit
      task = ""; tick = 0
      for (i = 1; i <= NF; i++) {
        if ($i ~ /^task=/) { split($i, a, "="); task = a[2] }
        if ($i ~ /^tick=/) { split($i, a, "="); tick = a[2] + 0 }
      }
      ms = parse_duration($NF)

      # Store per-task values.
      if (!(task in task_count)) { task_count[task] = 0; task_order[++num_tasks] = task }
      idx = task_count[task]++
      task_vals[task, idx] = ms
      task_ticks[task, idx] = tick

      # Store per-tick records for spike attribution.
      if (!(tick in tick_count)) tick_count[tick] = 0
      tc = tick_count[tick]++
      tick_tasks[tick, tc] = task
      tick_ms[tick, tc] = ms
    }
    END {
      if (num_tasks == 0) { print "EMPTY"; exit }

      for (t = 1; t <= num_tasks; t++) {
        task = task_order[t]
        n = task_count[task]

        # Find max before sorting.
        max_v = 0; max_tick = 0
        for (i = 0; i < n; i++) {
          if (task_vals[task, i] > max_v) {
            max_v = task_vals[task, i]
            max_tick = task_ticks[task, i]
          }
        }

        # Copy to temp array and sort.
        for (i = 0; i < n; i++) sorted[i] = task_vals[task, i]
        combsort(sorted, n)

        printf "TASK\t%s\t%d\t%.4f\t%.4f\t%.4f\t%.4f\t%.4f\t%d\n", \
          task, n, percentile(sorted,n,50), percentile(sorted,n,90), \
          percentile(sorted,n,95), percentile(sorted,n,99), max_v, max_tick
      }

      # Output per-tick records for spike attribution.
      for (tick in tick_count) {
        tc = tick_count[tick]
        for (i = 0; i < tc; i++) {
          printf "TICKCALL\t%d\t%s\t%.4f\n", tick, tick_tasks[tick, i], tick_ms[tick, i]
        }
      }
    }
  ' || true)

  if [[ -n "$per_call_raw" && "$per_call_raw" != "EMPTY" ]]; then
    # Display per-call stats and write CSV.
    while IFS=$'\t' read -r tag rest; do
      [[ "$tag" != "TASK" ]] && continue
      IFS=$'\t' read -r pc_task pc_count pc_p50 pc_p90 pc_p95 pc_p99 pc_max pc_max_tick <<< "$rest"
      printf "${C_GREEN}  Heavy per-call: %-25s count=%5d p50=%s p90=%s p95=%s p99=%s max=%s ms (tick %s)${C_RESET}\n" \
        "$pc_task" "$pc_count" "$pc_p50" "$pc_p90" "$pc_p95" "$pc_p99" "$pc_max" "$pc_max_tick"
      printf "%s,%s,%s,%s,%d,%s,%s,%s,%s,%s,%s\n" \
        "$ts" "$label" "$cstr" "$pc_task" "$pc_count" "$pc_p50" "$pc_p90" "$pc_p95" "$pc_p99" "$pc_max" "$pc_max_tick" \
        >> "$PER_CALL_CSV"
    done <<< "$per_call_raw"

    # Spike attribution.
    if [[ ${#spike_ticks[@]} -gt 0 ]]; then
      printf "${C_YELLOW}  Spike attribution:${C_RESET}\n"
      for spike in $(printf '%s\n' "${spike_ticks[@]}" | sort -n); do
        # Collect heavy task calls for this tick.
        parts=""
        heavy_sum="0"
        while IFS=$'\t' read -r tag tc_tick tc_task tc_ms; do
          [[ "$tag" != "TICKCALL" ]] && continue
          [[ "$tc_tick" != "$spike" ]] && continue
          parts="${parts:+${parts}, }${tc_task}=${tc_ms} ms"
          heavy_sum=$(awk "BEGIN{printf \"%.2f\", $heavy_sum + $tc_ms}")
        done <<< "$per_call_raw"

        if [[ -z "$parts" ]]; then
          printf "${C_DYELLOW}    tick %s: no heavy task calls (light tasks only)${C_RESET}\n" "$spike"
        else
          printf "${C_DYELLOW}    tick %s: heavy_sum=%s ms  (%s)${C_RESET}\n" "$spike" "$heavy_sum" "$parts"
        fi
      done
    fi
  fi

done

