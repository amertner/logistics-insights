function Write-OverrideFile {
  param([hashtable]$Config, [bool]$EnableProfiler, [int]$DumpAfterTicks, [string[]]$DisableTasksList)
  $lines = @()
  $lines += "-- Auto-generated by bench/run-benchmarks.ps1. Do not edit."
  $lines += "-- Label: $($Config.label)"
  if ($EnableProfiler) {
    $lines += "local bench_profiler = require('scripts.bench-profiler')"
    $lines += "bench_profiler.enabled = true"
    # Deadline: dump exactly once near the end of the benchmark, instead of
    # once per second. Eliminates ~45ms of measurement self-overhead per run.
    $lines += "bench_profiler.dump_after_ticks = $DumpAfterTicks"
    if ($null -ne $DisableTasksList -and $DisableTasksList.Count -gt 0) {
      # Replace each named task's interval with a value so large it never
      # fires within the benchmark. Patches scheduler.register at the moment
      # tasks are defined (bench-overrides loads before any register call).
      $lines += "bench_profiler.task_interval_overrides = {"
      foreach ($t in $DisableTasksList) {
        $lines += ("  ['" + $t + "'] = 999999999,")
      }
      $lines += "}"
    }
  }
  $lines += "return function(global_data)"
  foreach ($key in $Config.Keys) {
    if ($key -eq "label") { continue }
    $value = $Config[$key]
    $lua = if ($value -is [bool]) { if ($value) { "true" } else { "false" } } else { "$value" }
    $lines += "  global_data.$key = function() return $lua end"
  }
  $lines += "end"
  Set-Content -Path $OverrideLua -Value ($lines -join "`r`n") -Encoding UTF8
}

# Parse [libench-dump] lines from factorio-current.log. The mod emits one line
# per task per dump (Factorio caps localised strings at 20 parameters, so a
# single combined line is not possible once we have more than ~3 tasks).
#
# Lines look like:
#   [libench-dump] tick=<n> task=<name> count=<count> time=Duration: <value><unit>
#
# Factorio's LuaProfiler stringifies as e.g. "Duration: 16.101400ms" or
# "Duration: 678us" (no space between value and unit, "Duration: " prefix).
#
# This function finds the highest tick value, then collects all lines with that
# tick into a hashtable of task_name -> @{ count; total_ms; tick }.
function Read-BenchProfilerDump {
  param([string]$LogPath)
  if (-not (Test-Path $LogPath)) {
    Write-Warning "Bench profiler dump requested but log not found at $LogPath"
    return $null
  }

  $lineRegex = "\[libench-dump\] tick=(\d+) task=(\S+) count=(\d+) time=Duration:\s*([\d.]+)\s*(\w+)"
  $bestTick = -1
  $allMatches = @()
  foreach ($line in Get-Content -Path $LogPath) {
    if ($line -match $lineRegex) {
      $tick = [int]$Matches[1]
      $allMatches += [pscustomobject]@{
        tick  = $tick
        name  = $Matches[2]
        count = [int]$Matches[3]
        value = [double]$Matches[4]
        unit  = $Matches[5]
      }
      if ($tick -gt $bestTick) { $bestTick = $tick }
    }
  }
  if ($bestTick -lt 0) { return $null }

  $result = [ordered]@{}
  foreach ($m in $allMatches) {
    if ($m.tick -ne $bestTick) { continue }
    $totalMs = switch ($m.unit) {
      "s"  { $m.value * 1000.0 }
      "ms" { $m.value }
      "us" { $m.value / 1000.0 }
      "ns" { $m.value / 1000000.0 }
      default { $m.value }
    }
    $result[$m.name] = @{ count = $m.count; total_ms = $totalMs; tick = $bestTick }
  }
  return $result
}

# Parse [libench-tick] lines and compute percentile stats. Each line is one
# tick of LI's per-tick total cost, e.g.
#   [libench-tick] tick=24862341 time=Duration: 1.234ms
# These are all emitted in a single batch at the end of the run by
# scripts/bench-profiler.lua dump().
function Read-BenchTickStats {
  param([string]$LogPath)
  if (-not (Test-Path $LogPath)) { return $null }
  $regex = "\[libench-tick\] tick=(\d+) time=Duration:\s*([\d.]+)\s*(\w+)"
  $values = New-Object System.Collections.Generic.List[double]
  $ticks  = New-Object System.Collections.Generic.List[int]
  foreach ($line in Get-Content -Path $LogPath) {
    if ($line -match $regex) {
      $tick = [int]$Matches[1]
      $v    = [double]$Matches[2]
      $unit = $Matches[3]
      $ms = switch ($unit) {
        "s"  { $v * 1000.0 }
        "ms" { $v }
        "us" { $v / 1000.0 }
        "ns" { $v / 1000000.0 }
        default { $v }
      }
      $values.Add($ms)
      $ticks.Add($tick)
    }
  }
  $n = $values.Count
  if ($n -eq 0) { return $null }

  # Find max and the tick it occurred on (before sorting, since we need the index).
  $maxV = 0.0
  $maxTick = 0
  for ($i = 0; $i -lt $n; $i++) {
    if ($values[$i] -gt $maxV) {
      $maxV = $values[$i]
      $maxTick = $ticks[$i]
    }
  }

  # Sort for percentile lookup.
  $sorted = [double[]]::new($n)
  $values.CopyTo($sorted, 0)
  [Array]::Sort($sorted)

  function Get-Pct([double[]]$arr, [double]$p) {
    $idx = [Math]::Max(0, [Math]::Ceiling($arr.Length * $p / 100.0) - 1)
    return $arr[$idx]
  }

  $over5 = 0; $over10 = 0; $over50 = 0
  $spikeTicks = New-Object System.Collections.Generic.List[int]
  for ($i = 0; $i -lt $n; $i++) {
    $v = $sorted[$i]
    if ($v -gt 50) { $over50++ }
    if ($v -gt 10) { $over10++ }
    if ($v -gt  5) { $over5++ }
  }
  # Walk the unsorted arrays to collect actual tick numbers for spike ticks.
  for ($i = 0; $i -lt $n; $i++) {
    if ($values[$i] -gt 5) { $spikeTicks.Add($ticks[$i]) }
  }

  return [pscustomobject]@{
    active_ticks = $n
    p50          = Get-Pct $sorted 50
    p90          = Get-Pct $sorted 90
    p95          = Get-Pct $sorted 95
    p99          = Get-Pct $sorted 99
    max          = $maxV
    max_tick     = $maxTick
    over_5ms     = $over5
    over_10ms    = $over10
    over_50ms    = $over50
    spike_ticks  = $spikeTicks
  }
}

# Parse [libench-queue] lines and summarize. Format:
#   [libench-queue] first=N last=N max_heavy=N max_heavy_tick=N overflow=N
# Returns a summary object with: queue_count, total_overflow, queues_with_overflow,
# worst_max_heavy, worst_max_heavy_tick, plus the raw records for spike correlation.
function Read-BenchQueueStats {
  param([string]$LogPath)
  if (-not (Test-Path $LogPath)) { return $null }
  $regex = "\[libench-queue\] first=(\d+) last=(\d+) max_heavy=(\d+) max_heavy_tick=(\d+) overflow=(\d+)"
  $records = New-Object System.Collections.Generic.List[object]
  foreach ($line in Get-Content -Path $LogPath) {
    if ($line -match $regex) {
      $records.Add([pscustomobject]@{
        first          = [int]$Matches[1]
        last           = [int]$Matches[2]
        max_heavy      = [int]$Matches[3]
        max_heavy_tick = [int]$Matches[4]
        overflow       = [int]$Matches[5]
      })
    }
  }
  if ($records.Count -eq 0) { return $null }
  $totalOverflow = 0
  $queuesWithOverflow = 0
  $worstMax = 0
  $worstMaxTick = 0
  foreach ($r in $records) {
    $totalOverflow += $r.overflow
    if ($r.overflow -gt 0) { $queuesWithOverflow++ }
    if ($r.max_heavy -gt $worstMax) {
      $worstMax = $r.max_heavy
      $worstMaxTick = $r.max_heavy_tick
    }
  }
  return [pscustomobject]@{
    queue_count          = $records.Count
    total_overflow       = $totalOverflow
    queues_with_overflow = $queuesWithOverflow
    worst_max_heavy      = $worstMax
    worst_max_heavy_tick = $worstMaxTick
    records              = $records
  }
}

# Given a list of "spike" tick numbers and the queue records, find which queue
# each spike fell inside and report the max_heavy and overflow for that queue.
# This is the smoking-gun correlation: if every spike tick lands in a queue
# with overflow > 0 and high max_heavy, the scheduler is the cause.
function Find-SpikeQueues {
  param([int[]]$SpikeTicks, $QueueStats)
  if ($null -eq $QueueStats) { return @() }
  $hits = @()
  foreach ($tick in $SpikeTicks) {
    foreach ($q in $QueueStats.records) {
      if ($tick -ge $q.first -and $tick -le $q.last) {
        $hits += [pscustomobject]@{
          spike_tick     = $tick
          queue_first    = $q.first
          queue_last     = $q.last
          max_heavy      = $q.max_heavy
          max_heavy_tick = $q.max_heavy_tick
          overflow       = $q.overflow
          is_max_tick    = ($tick -eq $q.max_heavy_tick)
        }
        break
      }
    }
  }
  return $hits
}

# Parse [libench-call] lines and group by task. Each line is one heavy-task call:
#   [libench-call] task=<name> tick=<n> time=Duration: <value><unit>
# Returns:
#   per_task    = ordered hashtable: task_name -> @{ count, p50, p90, p95, p99, max, max_tick }
#   per_tick    = hashtable: tick -> array of @{ task; ms }
function Read-BenchPerCallStats {
  param([string]$LogPath)
  if (-not (Test-Path $LogPath)) { return $null }
  $regex = "\[libench-call\] task=(\S+) tick=(\d+) time=Duration:\s*([\d.]+)\s*(\w+)"
  $byTask = @{}
  $byTick = @{}
  foreach ($line in Get-Content -Path $LogPath) {
    if ($line -match $regex) {
      $task = $Matches[1]
      $tick = [int]$Matches[2]
      $v    = [double]$Matches[3]
      $unit = $Matches[4]
      $ms = switch ($unit) {
        "s"  { $v * 1000.0 }
        "ms" { $v }
        "us" { $v / 1000.0 }
        "ns" { $v / 1000000.0 }
        default { $v }
      }
      if (-not $byTask.ContainsKey($task)) {
        $byTask[$task] = @{
          values = New-Object System.Collections.Generic.List[double]
          ticks  = New-Object System.Collections.Generic.List[int]
        }
      }
      $byTask[$task].values.Add($ms)
      $byTask[$task].ticks.Add($tick)
      if (-not $byTick.ContainsKey($tick)) { $byTick[$tick] = @() }
      $byTick[$tick] += [pscustomobject]@{ task = $task; ms = $ms }
    }
  }
  if ($byTask.Count -eq 0) { return $null }

  function Get-PctL([double[]]$arr, [double]$p) {
    $idx = [Math]::Max(0, [Math]::Ceiling($arr.Length * $p / 100.0) - 1)
    return $arr[$idx]
  }

  $stats = [ordered]@{}
  foreach ($task in ($byTask.Keys | Sort-Object)) {
    $entry = $byTask[$task]
    $n = $entry.values.Count
    $maxV = 0.0
    $maxTick = 0
    for ($i = 0; $i -lt $n; $i++) {
      if ($entry.values[$i] -gt $maxV) {
        $maxV = $entry.values[$i]
        $maxTick = $entry.ticks[$i]
      }
    }
    $sorted = [double[]]::new($n)
    $entry.values.CopyTo($sorted, 0)
    [Array]::Sort($sorted)
    $stats[$task] = [pscustomobject]@{
      count    = $n
      p50      = Get-PctL $sorted 50
      p90      = Get-PctL $sorted 90
      p95      = Get-PctL $sorted 95
      p99      = Get-PctL $sorted 99
      max      = $maxV
      max_tick = $maxTick
    }
  }

  return [pscustomobject]@{
    per_task = $stats
    per_tick = $byTick
  }
}

function Read-BenchmarkOutput {
  param([string]$Output)
  # Factorio's --benchmark output ends with lines like:
  #   Performed 6000 updates in 1234.567 ms
  #   avg: 0.206 ms, min: 0.150 ms, max: 1.234 ms
  $avg = $null; $min = $null; $max = $null; $totalMs = $null; $updates = $null
  foreach ($line in $Output -split "`n") {
    if ($line -match "Performed\s+(\d+)\s+updates in\s+([\d.]+)\s*ms") {
      $updates = [int]$Matches[1]
      $totalMs = [double]$Matches[2]
    }
    if ($line -match "avg:\s*([\d.]+)\s*ms.*min:\s*([\d.]+)\s*ms.*max:\s*([\d.]+)\s*ms") {
      $avg = [double]$Matches[1]
      $min = [double]$Matches[2]
      $max = [double]$Matches[3]
    }
  }
  return [pscustomobject]@{
    updates  = $updates
    total_ms = $totalMs
    avg_ms   = $avg
    min_ms   = $min
    max_ms   = $max
  }
}

# Write CSV headers if files do not exist.
if (-not (Test-Path $ResultsCsv)) {
  "timestamp,label,config,updates,total_ms,avg_ms,min_ms,max_ms" | Set-Content -Path $ResultsCsv -Encoding UTF8
}
if ($EnableBenchProfiler -and -not (Test-Path $PerTaskCsv)) {
  "timestamp,label,config,task,count,total_ms,avg_ms_per_call,dump_tick" | Set-Content -Path $PerTaskCsv -Encoding UTF8
}
if ($EnableBenchProfiler -and -not (Test-Path $TickStatsCsv)) {
  "timestamp,label,config,active_ticks,p50_ms,p90_ms,p95_ms,p99_ms,max_ms,max_tick,over_5ms,over_10ms,over_50ms" | Set-Content -Path $TickStatsCsv -Encoding UTF8
}
if ($EnableBenchProfiler -and -not (Test-Path $PerCallCsv)) {
  "timestamp,label,config,task,count,p50_ms,p90_ms,p95_ms,p99_ms,max_ms,max_tick" | Set-Content -Path $PerCallCsv -Encoding UTF8
}

try {
  foreach ($config in $Configurations) {
    Write-Host ""
    Write-Host "=== $($config.label) ===" -ForegroundColor Cyan
    # Dump 60 ticks (1s) before the benchmark ends, so the once-per-second
    # scheduler check is guaranteed to fire it before Factorio exits.
    $dumpAfter = $BenchmarkTicks - 60
    Write-OverrideFile -Config $config -EnableProfiler $EnableBenchProfiler -DumpAfterTicks $dumpAfter -DisableTasksList $DisableTasks

    # Truncate the Factorio log so the dump-parser only sees this run's output.
    if ($EnableBenchProfiler -and (Test-Path $FactorioLog)) {
      Clear-Content -Path $FactorioLog
    }

    $factorioArgs = @(
      "--benchmark", $SaveFile,
      "--benchmark-ticks", $BenchmarkTicks,
      "--benchmark-runs", $BenchmarkRuns,
      "--disable-audio"
    )
    Write-Host "Running: $FactorioExe $($factorioArgs -join ' ')"
    $output = & $FactorioExe @factorioArgs 2>&1 | Out-String
    $parsed = Read-BenchmarkOutput -Output $output

    if ($null -eq $parsed.avg_ms) {
      Write-Warning "Could not parse benchmark output for $($config.label). Raw output:"
      Write-Host $output
      continue
    }

    Write-Host ("  updates={0}  total={1} ms  avg={2} ms  min={3} ms  max={4} ms" -f `
      $parsed.updates, $parsed.total_ms, $parsed.avg_ms, $parsed.min_ms, $parsed.max_ms) -ForegroundColor Green

    $configStr = ($config.GetEnumerator() | Where-Object { $_.Key -ne "label" } | ForEach-Object { "$($_.Key)=$($_.Value)" }) -join ";"
    $timestamp = Get-Date -Format "o"
    $row = "{0},{1},{2},{3},{4},{5},{6},{7}" -f `
      $timestamp, $config.label, $configStr, $parsed.updates, $parsed.total_ms, $parsed.avg_ms, $parsed.min_ms, $parsed.max_ms
    Add-Content -Path $ResultsCsv -Value $row -Encoding UTF8

    if ($EnableBenchProfiler) {
      $taskTotals = Read-BenchProfilerDump -LogPath $FactorioLog
      if ($null -eq $taskTotals -or $taskTotals.Count -eq 0) {
        Write-Warning "No bench profiler dump found in $FactorioLog. Did the run reach 60 ticks?"
      } else {
        $firstEntry = $null
        foreach ($v in $taskTotals.Values) { $firstEntry = $v; break }
        Write-Host "  Per-task (dump @ tick $($firstEntry.tick)):" -ForegroundColor Green

        # Console: print only tasks above the noise threshold; collapse the rest
        # into one summary line. CSV write below is unfiltered.
        $omittedNames = @()
        $omittedTotal = 0.0
        foreach ($name in $taskTotals.Keys) {
          $entry = $taskTotals[$name]
          $avgPerCall = if ($entry.count -gt 0) { $entry.total_ms / $entry.count } else { 0 }
          if ($entry.total_ms -ge $PerTaskDisplayThresholdMs) {
            Write-Host ("    {0,-30} count={1,6}  total={2,9:F3} ms  avg={3,8:F4} ms/call" -f `
              $name, $entry.count, $entry.total_ms, $avgPerCall) -ForegroundColor DarkGreen
          } else {
            $omittedNames += $name
            $omittedTotal += $entry.total_ms
          }
          $taskRow = "{0},{1},{2},{3},{4},{5:F4},{6:F6},{7}" -f `
            $timestamp, $config.label, $configStr, $name, $entry.count, $entry.total_ms, $avgPerCall, $entry.tick
          Add-Content -Path $PerTaskCsv -Value $taskRow -Encoding UTF8
        }
        if ($omittedNames.Count -gt 0) {
          Write-Host ("    ({0} tasks omitted < {1} ms: {2} - {3:F1} ms total)" -f `
            $omittedNames.Count, $PerTaskDisplayThresholdMs, ($omittedNames -join ", "), $omittedTotal) -ForegroundColor DarkGray
        }
      }

      # Per-tick distribution stats from the same dump cycle.
      $tickStats = Read-BenchTickStats -LogPath $FactorioLog
      $queueStats = Read-BenchQueueStats -LogPath $FactorioLog

      # Queue summary: only print the noisy form when there is something
      # interesting (overflow happened, or two heavy tasks landed on one tick).
      # Otherwise print a one-line "healthy" message.
      if ($null -ne $queueStats) {
        if ($queueStats.total_overflow -gt 0 -or $queueStats.worst_max_heavy -gt 1) {
          Write-Host ("  Queue: builds={0} with_overflow={1} total_overflow={2} worst_max_heavy={3} (tick {4})" -f `
            $queueStats.queue_count, $queueStats.queues_with_overflow, $queueStats.total_overflow, `
            $queueStats.worst_max_heavy, $queueStats.worst_max_heavy_tick) -ForegroundColor Yellow
        } else {
          Write-Host ("  Queue: builds={0} (no collisions, no overflow)" -f $queueStats.queue_count) -ForegroundColor DarkGreen
        }
      }
      if ($null -ne $tickStats) {
        Write-Host ("  LI per-tick: active={0} p50={1:F3} p90={2:F3} p95={3:F3} p99={4:F3} max={5:F3} ms (tick {6})  >5ms:{7} >10ms:{8} >50ms:{9}" -f `
          $tickStats.active_ticks, $tickStats.p50, $tickStats.p90, $tickStats.p95, $tickStats.p99, $tickStats.max, $tickStats.max_tick, `
          $tickStats.over_5ms, $tickStats.over_10ms, $tickStats.over_50ms) -ForegroundColor Green

        # Spike correlation: which queue scheduled each spike tick? Only useful
        # when the queue actually had collisions or overflow; otherwise the
        # answer is always "uncorrelated" and the line is noise.
        if ($null -ne $queueStats -and ($queueStats.total_overflow -gt 0 -or $queueStats.worst_max_heavy -gt 1) -and $tickStats.spike_ticks.Count -gt 0) {
          $hits = Find-SpikeQueues -SpikeTicks $tickStats.spike_ticks -QueueStats $queueStats
          if ($hits.Count -gt 0) {
            $sameAsMax = ($hits | Where-Object { $_.is_max_tick }).Count
            $withOverflow = ($hits | Where-Object { $_.overflow -gt 0 }).Count
            Write-Host ("  Spike correlation: {0} spike ticks; {1} hit their queue's max_heavy_tick; {2} were in queues with overflow > 0" -f `
              $hits.Count, $sameAsMax, $withOverflow) -ForegroundColor Yellow
          }
        }

        $tickRow = "{0},{1},{2},{3},{4:F4},{5:F4},{6:F4},{7:F4},{8:F4},{9},{10},{11},{12}" -f `
          $timestamp, $config.label, $configStr, $tickStats.active_ticks, `
          $tickStats.p50, $tickStats.p90, $tickStats.p95, $tickStats.p99, $tickStats.max, $tickStats.max_tick, `
          $tickStats.over_5ms, $tickStats.over_10ms, $tickStats.over_50ms
        Add-Content -Path $TickStatsCsv -Value $tickRow -Encoding UTF8
      }

      # Per-call distribution for heavy tasks. Answers "what does the per-call
      # distribution actually look like" and "which heavy task call landed in
      # this spike tick".
      $perCall = Read-BenchPerCallStats -LogPath $FactorioLog
      if ($null -ne $perCall) {
        foreach ($task in $perCall.per_task.Keys) {
          $s = $perCall.per_task[$task]
          Write-Host ("  Heavy per-call: {0,-25} count={1,5} p50={2:F3} p90={3:F3} p95={4:F3} p99={5:F3} max={6:F3} ms (tick {7})" -f `
            $task, $s.count, $s.p50, $s.p90, $s.p95, $s.p99, $s.max, $s.max_tick) -ForegroundColor Green
          $callRow = "{0},{1},{2},{3},{4},{5:F4},{6:F4},{7:F4},{8:F4},{9:F4},{10}" -f `
            $timestamp, $config.label, $configStr, $task, $s.count, $s.p50, $s.p90, $s.p95, $s.p99, $s.max, $s.max_tick
          Add-Content -Path $PerCallCsv -Value $callRow -Encoding UTF8
        }

        # Spike attribution: for each spike tick, list which heavy task calls landed there.
        if ($null -ne $tickStats -and $tickStats.spike_ticks.Count -gt 0) {
          Write-Host "  Spike attribution:" -ForegroundColor Yellow
          # Build a quick lookup of LI tick total ms for residual computation.
          $liByTick = @{}
          for ($i = 0; $i -lt $tickStats.spike_ticks.Count; $i++) {
            $liByTick[$tickStats.spike_ticks[$i]] = $true
          }
          # We need actual per-tick totals; reuse the unsorted arrays from
          # tickStats (we kept ms+tick parallel in Read-BenchTickStats).
          # Since tickStats only exposes the spike_ticks list and not the
          # corresponding ms values, re-derive from per_call (an approximation
          # equal to the sum of heavy tasks landing on that tick).
          foreach ($t in ($tickStats.spike_ticks | Sort-Object)) {
            $hits = $perCall.per_tick[$t]
            if ($null -eq $hits) {
              Write-Host ("    tick {0}: no heavy task calls (light tasks only)" -f $t) -ForegroundColor DarkYellow
              continue
            }
            $parts = @()
            $sum = 0.0
            foreach ($h in $hits) {
              $parts += ("{0}={1:F2} ms" -f $h.task, $h.ms)
              $sum += $h.ms
            }
            Write-Host ("    tick {0}: heavy_sum={1:F2} ms  ({2})" -f $t, $sum, ($parts -join ", ")) -ForegroundColor DarkYellow
          }
        }
      }

    }
  }
}
finally {
  if (Test-Path $OverrideLua) {
    Remove-Item $OverrideLua -Force
    Write-Host ""
    Write-Host "Removed $OverrideLua" -ForegroundColor DarkGray
  }
}

Write-Host ""
Write-Host "Done. Results appended to $ResultsCsv" -ForegroundColor Cyan
if ($EnableBenchProfiler) {
  Write-Host "Per-task results appended to $PerTaskCsv" -ForegroundColor Cyan
  Write-Host "Per-tick stats appended to $TickStatsCsv" -ForegroundColor Cyan
  Write-Host "Per-call stats appended to $PerCallCsv" -ForegroundColor Cyan
}
