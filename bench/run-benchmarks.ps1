<#
.SYNOPSIS
  Sweep logistics-insights mod settings against a Factorio save and record timings.

.DESCRIPTION
  For each configuration in $Configurations, this script:
    1. Writes bench-overrides.lua in the mod folder with the corresponding accessor overrides.
    2. Runs Factorio with --benchmark and captures stdout.
    3. Parses the avg/min/max ms-per-update from the benchmark summary.
    4. Appends a row to bench/results.csv.

  bench-overrides.lua is removed in the finally block, even if the user hits Ctrl-C.

.NOTES
  Configure the variables in the CONFIGURATION section below before running.
#>

